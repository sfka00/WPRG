        <h3>Początki UFC</h3>
        <p>UFC została założona w 1993 roku przez Arta Davie, Roriona Gracie i Boba Meyrowitza,
            aby stworzyć turniej konfrontujący różne style walki w celu ustalenia, który jest najskuteczniejszy w prawdziwej walce.
            Pierwsze wydarzenie, UFC 1, odbyło się w Denver w stanie Kolorado i miało formułę turnieju bez podziału na kategorie wagowe,
            z minimalnymi zasadami i brakiem ograniczeń co do technik.
        </p>
