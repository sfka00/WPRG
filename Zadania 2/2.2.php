    <h2>UFC</h2>
    <p>UFC (Ultimate Fighting Championship) jest obecnie najważniejszą organizacją mieszanych sztuk walki (MMA) na świecie.
        Założona w 1993 roku, przeszła drogę od kontrowersyjnego i eksperymentalnego wydarzenia do jednej z najbardziej rozpoznawalnych platform sportowych
        na całym globie. Niniejszy artykuł przedstawia jej fascynującą ewolucję, analizując kluczowe momenty i postaci, które ukształtowały jej historię.</p>
